    private void fillBancos(JComboBox box) {
        String sql = "select banco from bancos order by banco;";
        ResultSet crs = conn.AbrirTabela(sql, ResultSet.CONCUR_READ_ONLY);
        try {
            while (crs.next()) {
                box.addItem(crs.getString("banco"));
            }
        } catch (Exception e) {}
        DbMain.FecharTabela(crs);
        
        box.setSelectedIndex(-1);
    }

