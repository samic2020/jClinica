    private void jbtLancarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtLancarActionPerformed
        this.CT_numero = jCT_numero.getText();
        try {this.CH_banco = jCH_banco.getSelectedItem().toString();} catch (NullPointerException e) {this.CH_banco = "";}
        this.CH_agencia = jCH_agencia.getText();
        this.CH_numero = jCH_numero.getText();
        this.CH_data = Dates.DateFormat("dd/MM/yyyy",jCH_data.getDate());
        this.VR = jOP.getValue();
        dispose();
    }//GEN-LAST:event_jbtLancarActionPerformed

    private void jbtCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtCancelarActionPerformed
        if (this.tipo <= 0) dispose();
        if (this.tipo > 0) {
            Selecao(0);
        }
    }//GEN-LAST:event_jbtCancelarActionPerformed

