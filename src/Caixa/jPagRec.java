    private void jCT_numeroFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jCT_numeroFocusLost
        String ct = jCT_numero.getText().trim();
        ct = FuncoesGlobais.StrZero(ct, 16);
        jCT_numero.setText(ct);
        jbtLancar.setEnabled(LerValor.StringToFloat(jVR.getText()) >= LerValor.StringToFloat(jOP.getText()));
        jbtLancar.requestFocus();
    }//GEN-LAST:event_jCT_numeroFocusLost

