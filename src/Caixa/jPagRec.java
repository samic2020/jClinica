    private void jVRFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jVRFocusLost
        try {
            jbtLancar.setEnabled(LerValor.StringToFloat(jVR.getText()) >= LerValor.StringToFloat(jOP.getText()));
            jTR.setText(LerValor.floatToCurrency(LerValor.StringToFloat(jVR.getText()) - LerValor.StringToFloat(jOP.getText()),2));
            if (LerValor.StringToFloat(jVR.getText()) - LerValor.StringToFloat(jOP.getText()) >= 0) {
                jTR.setForeground(Color.blue);
                jTRlabel.setText("Troco:");
            } else {
                jTR.setForeground(Color.red);
                jTRlabel.setText("Falta:");
                JOptionPane.showMessageDialog(this, "Valor digitado a menor!","Erro",ERROR_MESSAGE);
                jVR.requestFocus();
            }
        } catch (NullPointerException e) {jbtLancar.setEnabled(false);}
    }//GEN-LAST:event_jVRFocusLost

    private void jVRKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jVRKeyReleased
        if (evt.getKeyCode() == KeyEvent.VK_ENTER && LerValor.db2Float(jVR.getText()) > 0) {
            jbtLancar.requestFocus();
        }
    }//GEN-LAST:event_jVRKeyReleased

    private void Selecao(int tipo) {
        if (tipo == 0) {
            // Habilitados
            jDN.setEnabled(true);
            jCH.setEnabled(true);
            jCT.setEnabled(true);

            jCT_numero.setText(null); jCT_numero.setEnabled(false);
            jCH_banco.setSelectedIndex(-1); jCH_banco.setEnabled(false);
            jCH_agencia.setText(null); jCH_agencia.setEnabled(false);
            jCH_numero.setText(null); jCH_numero.setEnabled(false);
            jCH_data.setDate(new Date()); jCH_data.setEnabled(false);
            jVR.setText("0,00"); jVR.setEnabled(false); jVR.setValue(0);
            jTR.setText("0,00"); jTR.setForeground(Color.black); 
            jTR.setVisible(true); jTRlabel.setVisible(true);
            
            jbtLancar.setEnabled(false);
            this.tipo = 0;
        } else if (tipo == 1) {
            // Dinheiro
            jDN.setEnabled(true);
            jCH.setEnabled(false);
            jCT.setEnabled(false);
            
            jCT_numero.setText(null); jCT_numero.setEnabled(false);
            jCH_banco.setSelectedIndex(-1); jCH_banco.setEnabled(false);
            jCH_agencia.setText(null); jCH_agencia.setEnabled(false);
            jCH_numero.setText(null); jCH_numero.setEnabled(false);
            jCH_data.setDate(new Date()); jCH_data.setEnabled(false);
            jVR.setText("0,00"); jVR.setEnabled(true); 
            jVR.setValue(0); jVR.setEditable(true); 
            jTR.setText("0,00"); jTR.setForeground(Color.black); 
            jTR.setVisible(true); jTRlabel.setVisible(true);
            jVR.requestFocus();
            
            jbtLancar.setEnabled(false);
            this.tipo = 1;
        } else if (tipo == 2) {
            // Cheque
            jDN.setEnabled(false);
            jCH.setEnabled(true);
            jCT.setEnabled(false);

            jCT_numero.setText(null); jCT_numero.setEnabled(false);
            jCH_banco.setSelectedIndex(-1); jCH_banco.setEnabled(true);
            jCH_agencia.setText(null); jCH_agencia.setEnabled(true);
            jCH_numero.setText(null); jCH_numero.setEnabled(true);
            jCH_data.setDate(new Date()); jCH_data.setEnabled(true);
            
            try {jVR.setText(LerValor.floatToCurrency(LerValor.StringToFloat(jOP.getText()),2));} catch (NullPointerException e) {}
            jVR.setEnabled(true); jVR.setEditable(false);
            jTR.setText("0,00"); jTR.setForeground(Color.black); 
            jTR.setVisible(false); jTRlabel.setVisible(false);
            
            jCH_banco.requestFocus();
            
            jbtLancar.setEnabled(false);
            this.tipo = 2;
        } else if (tipo == 3) {
            // Cartão
            jDN.setEnabled(false);
            jCH.setEnabled(false);
            jCT.setEnabled(true);
            
            jCT_numero.setText(null); jCT_numero.setEnabled(true);
            jCH_banco.setSelectedIndex(-1); jCH_banco.setEnabled(false);
            jCH_agencia.setText(null); jCH_agencia.setEnabled(false);
            jCH_numero.setText(null); jCH_numero.setEnabled(false);
            jCH_data.setDate(new Date()); jCH_data.setEnabled(false);
            
            try {jVR.setText(LerValor.floatToCurrency(LerValor.StringToFloat(jOP.getText()),2));} catch (NullPointerException e) {}
            jVR.setEnabled(true); jVR.setEditable(false);
            jTR.setText("0,00"); jTR.setForeground(Color.black); 
            jTR.setVisible(false); jTRlabel.setVisible(false);
            
            jCT_numero.requestFocus();
            
            jbtLancar.setEnabled(false);
            this.tipo = 3;
        }
    }
    
