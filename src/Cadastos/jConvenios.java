    private void btGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGravarActionPerformed
        String tsql = "UPDATE convenios SET cv_ans = ?, cv_codtabelas = ?, " +
            "cv_codoperadora = ?, cv_numerador = ?, cv_consultavr = ?, cv_medicovr = ? WHERE cv_numero = ?;";
        Object[][] param = new Object[][] {
                {"string",cvAns.getText()},
                {"string",cvTabela.getText()},
                {"string", cvCodnoConv.getText()},
                {"int", Integer.parseInt(cvNumLote.getText().trim())},
                {"decimal", new BigDecimal(mttxbValor.getText().replace(".", "").replace(",", "."))},
                {"decimal", new BigDecimal(mttxbValorMed.getText().replace(".", "").replace(",", "."))},
                {"int",Integer.valueOf(codConv.getText())}
            };
        try { conn.ExecutarComando(tsql, param); } catch (Exception e) {}
    }//GEN-LAST:event_btGravarActionPerformed

    private void BuscarConvenio(String cvcodigo) {
        try {urs.first();} catch (SQLException e) {}
        try {
            while (urs.next()) {
                if (urs.getString("cv_numero").equalsIgnoreCase(cvcodigo)) {
                    LerConvenios();
                    break;
                }
            }
        } catch (SQLException e) {}
    }

    private void LimpaTela() {
        codConv.setText("");
        nmConv.setText("");
        cvAns.setText("");
        cvTabela.setText("");
        cvCodnoConv.setText("");
        cvNumLote.setText("0");
        mttxbValor.setText("0,00");
        mttxbValorMed.setText("0,00");
    }
    
    private void LerConvenios() {
        try { codConv.setText(urs.getString("cv_numero")); } catch (SQLException e) { codConv.setText("0"); }
        try { nmConv.setText(urs.getString("cv_convenio")); } catch (SQLException e) { nmConv.setText(""); }
        try { cvAns.setText(urs.getString("cv_ans")); } catch (SQLException e) { cvAns.setText(""); }
        try { cvTabela.setText(urs.getString("cv_codtabelas")); } catch (SQLException e) { cvTabela.setText(""); }
        try { cvCodnoConv.setText(urs.getString("cv_codoperadora")); } catch (SQLException e) { cvCodnoConv.setText(""); }
        try { cvNumLote.setText(String.valueOf(urs.getInt("cv_numerador"))); } catch (SQLException e) { cvNumLote.setText(""); }
        try { mttxbValor.setText(urs.getBigDecimal("cv_consultavr").toPlainString().toString().replace(".", ",")); } catch (SQLException e) { mttxbValor.setText("0,00"); }
        try { mttxbValorMed.setText(urs.getBigDecimal("cv_medicovr").toPlainString().toString().replace(".", ",")); } catch (SQLException e) { mttxbValorMed.setText("0,00"); }
    }
    
