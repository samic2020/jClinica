    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        if (bAlt) {
            Object[] options = { "Sim", "Não" };
            int n = JOptionPane.showOptionDialog(this, "Deseja gravar as alteração neste convênio ?", "Atenção", 0, 3, null, options, options[0]);
            if (n == 1) {
                rbtValor.setSelected(!tpTaxa);
                rbtValor.setEnabled(false);
                rbtPercent.setEnabled(false);
                tbxConsulta.setText(vrTaxaConsulta);
                tbxConsulta.setEnabled(false);                
                txbProced.setText(vrTaxaProced);
                txbProced.setEnabled(false);                
                txbConMed.setText(vrTxConMed);
                txbConMed.setEnabled(false);
                tbxProcMed.setText(vrTxProMed);
                tbxProcMed.setEnabled(false);

                tpTaxa = false;
                vrTaxaConsulta = "0,00";
                vrTaxaProced = "0,00";
                vrTxConMed = "0,00";
                vrTxProMed = "0,00";
            } else {
                String updateSQL = "update convenios set cv_tabelas = ?, cv_txconsulta = ?, cv_txproced = ?, cv_vrpconmed = ?, cv_vrppromed = ? where cv_numero = ?;";
                Object[][] param = new Object[][] {
                    {"string", (rbtValor.isSelected() ? "0" : "1")},
                    {"float", LerValor.StringToFloat(tbxConsulta.getText())},
                    {"float", LerValor.StringToFloat(txbProced.getText())},
                    {"float", LerValor.StringToFloat(txbConMed.getText())},
                    {"float", LerValor.StringToFloat(tbxProcMed.getText())},
                    {"int", Integer.valueOf(cbxCodConv.getSelectedItem().toString())}
                };
                conn.ExecutarComando(updateSQL, param);
                
                rbtValor.setEnabled(false);
                rbtPercent.setEnabled(false);
                tbxConsulta.setEnabled(false);                
                txbProced.setEnabled(false);
                txbConMed.setEnabled(false);                
                tbxProcMed.setEnabled(false);
                
                tpTaxa = false;
                vrTaxaConsulta = "0,00";
                vrTaxaProced = "0,00";
            }   
            bAlt = false;
            btnAlterar.setEnabled(true); btnGravar.setEnabled(false); btnSair.setEnabled(true);
            cbxCodConv.setEnabled(true); cbxNomeConv.setEnabled(true);
            cbxCodConv.requestFocus();
        }
    }//GEN-LAST:event_btnGravarActionPerformed

    private void SeekConv(String codConv) {
        String tsql = "select cv_tabelas, cv_txconsulta, cv_txproced, cv_vrpconmed, cv_vrppromed from convenios WHERE cv_numero = '" + codConv + "' ORDER BY Upper(Trim(cv_apelido));";
        ResultSet tbmedicos = conn.AbrirTabela(tsql, ResultSet.CONCUR_READ_ONLY);
        try {
            while (tbmedicos.next()) {
                String tTipoVr = tbmedicos.getString("cv_tabelas");
                double tvrConsulta = tbmedicos.getDouble("cv_txconsulta");
                double tvrProced = tbmedicos.getDouble("cv_txproced");

                double tvrConMed = tbmedicos.getDouble("cv_vrpconmed");
                double tvrProMed = tbmedicos.getDouble("cv_vrppromed");

                try {
                    if (tTipoVr.equalsIgnoreCase("0")) rbtValor.setSelected(true); else rbtPercent.setSelected(true);
                } catch (Exception e) { rbtPercent.setSelected(true); }
                
                tbxConsulta.setText(LerValor.floatToCurrency((float)tvrConsulta,2));
                txbProced.setText(LerValor.floatToCurrency((float)tvrProced,2));
                
                txbConMed.setText(LerValor.floatToCurrency((float)tvrConMed,2));
                tbxProcMed.setText(LerValor.floatToCurrency((float)tvrProMed,2));
            }
        } catch (Exception err) {err.printStackTrace();}
        DbMain.FecharTabela(tbmedicos);
    }

