    private void FillConvenios() {
        String tsql = "select cv_numero, cv_apelido from convenios WHERE Upper(Trim(cv_apelido)) != 'PARTICULAR' ORDER BY Upper(Trim(cv_apelido));";
        ResultSet tbmedicos = conn.AbrirTabela(tsql, 1007);
        try {
            while (tbmedicos.next()) {
                String tcodigo = tbmedicos.getString("cv_numero");
                String tconvenio = tbmedicos.getString("cv_apelido").toUpperCase();

                cbxCodConv.addItem(tcodigo);
                cbxNomeConv.addItem(tconvenio);
            }
        } catch (Exception err) {err.printStackTrace();}
        DbMain.FecharTabela(tbmedicos);        
    }
    
