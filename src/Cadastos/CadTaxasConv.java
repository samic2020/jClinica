    private void btnSairActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSairActionPerformed
        if (bAlt) {
            Object[] options = { "Sim", "Não" };
            int n = JOptionPane.showOptionDialog(this, "Deseja cancelar alteração deste convênio ?\n\nMUITO CUIDADO POIS TODOS OS DADOS LANÇADOS NESTE CONVENIO SERÃO PERDIDOS!!!", "Atenção", 0, 3, null, options, options[0]);
            if (n == 0) {
                rbtValor.setSelected(!tpTaxa);
                rbtValor.setEnabled(false);
                rbtPercent.setEnabled(false);
                tbxConsulta.setText(vrTaxaConsulta);
                tbxConsulta.setEnabled(false);                
                txbProced.setText(vrTaxaProced);
                txbProced.setEnabled(false);
                txbConMed.setText(vrTxConMed);
                txbConMed.setEnabled(false);
                tbxProcMed.setText(vrTxProMed);
                tbxProcMed.setEnabled(false);
                
                tpTaxa = false;
                vrTaxaConsulta = "0,00";
                vrTaxaProced = "0,00";
                vrTxConMed = "0,00";
                vrTxProMed = "0,00";
                
                cbxCodConv.requestFocus();
            }   
            bAlt = false;
            btnAlterar.setEnabled(true); btnGravar.setEnabled(false); btnSair.setEnabled(true);
            cbxCodConv.setEnabled(true); cbxNomeConv.setEnabled(true);
            cbxCodConv.requestFocus();
        } else {
            dispose();
        }
    }//GEN-LAST:event_btnSairActionPerformed

