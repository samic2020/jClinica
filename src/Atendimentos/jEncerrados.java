    public void setLista(String[][] lista) { listaespera = lista; }

    public void ShowLista() {
        for (int i = 0; i < listaespera.length; i++) {
            String tentrada = listaespera[i][0];
            String tsaida = listaespera[i][1];
            String tnome = listaespera[i][2].toUpperCase().trim();
            String tmedico = listaespera[i][3].toUpperCase().trim();

            TableControl.add(clvAtendidos, new String[][] { { tentrada, tsaida, tnome, tmedico }, { "C", "C", "L", "L" } }, true);
        }
        sorter = new TableRowSorter(clvAtendidos.getModel());
        clvAtendidos.setRowSorter(sorter);
    }

