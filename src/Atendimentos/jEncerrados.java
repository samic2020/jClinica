    private void txbAtendidosKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txbAtendidosKeyReleased
        if ("".equals(this.txbAtendidos.getText().trim())) {
            this.sorter.setRowFilter(null);
        } else {
            try {
                this.sorter.setRowFilter(RowFilter.regexFilter(this.txbAtendidos.getText().trim(), new int[0]));
            } catch (PatternSyntaxException pse) {
                System.err.println("Bad regex pattern");
            }
        }
    }//GEN-LAST:event_txbAtendidosKeyReleased

