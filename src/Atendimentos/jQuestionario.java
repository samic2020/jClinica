    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
      String sql = "UPDATE pacientes SET pc_escsimnao=?, pc_bensimnao=?, pc_benqual=?, pc_trabsimnao=?, pc_trabformsimnao=?, pc_drogsimnao=?, pc_drogqual=? WHERE pc_numero=?;";
      try {
        PreparedStatement stm = conn.conn.prepareStatement(sql);
        stm.setBoolean(1, escSim.isSelected());
        stm.setBoolean(2, benefSim.isSelected());
        stm.setString(3, benefQual.getText());
        stm.setBoolean(4, trabSim.isSelected());
        stm.setBoolean(5, formSim.isSelected());
        stm.setBoolean(6, drogSim.isSelected());
        stm.setString(7, drogQual.getText());
        stm.setDouble(8, Double.valueOf(this.cdPac));
        stm.execute();
        stm.close();
      } catch (SQLException e) {e.printStackTrace();}
      dispose();
    }//GEN-LAST:event_btnGravarActionPerformed

