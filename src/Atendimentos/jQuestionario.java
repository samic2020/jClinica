    public void LerQuestionario() {
        ResultSet rs = conn.AbrirTabela("SELECT pc_escsimnao, pc_bensimnao, pc_benqual, pc_trabsimnao, pc_trabformsimnao, pc_drogsimnao, pc_drogqual FROM pacientes WHERE pc_numero = Cast('" + this.cdPac + "' AS double precision);", ResultSet.CONCUR_READ_ONLY);
        try {
            while (rs.next()) {
                if (rs.getBoolean("pc_escsimnao")) {
                    escSim.setSelected(true);
                } else escNao.setSelected(true);
                if (rs.getBoolean("pc_bensimnao")) {
                    benefSim.setSelected(true);
                    benefQual.setText(rs.getString("pc_benqual"));
                } else {
                    benefNao.setSelected(true);
                    benefQual.setText("");
                }
                if (rs.getBoolean("pc_trabsimnao")) {
                    trabSim.setSelected(true);
                } else trabNao.setSelected(true);
                if (rs.getBoolean("pc_trabformsimnao")) {
                    formSim.setSelected(true);
                } else formNao.setSelected(true);
                if (rs.getBoolean("pc_drogsimnao")) {
                    drogSim.setSelected(true);
                    drogQual.setText(rs.getString("pc_drogqual"));
                } else {
                    drogNao.setSelected(true);
                    drogQual.setText("");
                }                
            }
        } catch (Exception e) {e.printStackTrace();}
        DbMain.FecharTabela(rs);
    }
    
